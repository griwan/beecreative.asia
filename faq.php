<h1 class="page-header">More About BC</h1>

<h4><a href="##" data-toggle="collapse" data-target=".1">What is the BC program?</a></h4>
<div class="collapse 1">BeeCreative is a co-curricular program in which our teachers carry kits and come to your school to teach your students in your own classroom. The classes are hands-on, interactive, and related to the curriculum.
</div> 
<hr>
<h4><a href="##" data-toggle="collapse" data-target=".2">What classes is it appropriate for?</a></h4>
<div class="collapse 2">The BeeCreative program is currently designed for students between grades 5 and 8. We are expanding the program for lower grades.
 </div>
 <hr>
<h4><a href="##" data-toggle="collapse" data-target=".3">How is BC delivered inside my school?</a></h4>
<div class="collapse 3"><p>One period a week is set aside for BC. This one period is for each class/section that BC is used for. Let’s say your school has two sections and runs BC for grades 6 & 7 the schedule might look like this:</p>
<p>Tuesday 2nd period (9.30 AM - 10.15 AM): 6A</p>
<p>Tuesday 3rd period (10.25 AM - 11:10 AM): 6B</p>
<p>Tuesday 5th period (12.00 PM - 12:45 PM): 7A</p>
<p>Tuesday 6th period (12.45 PM - 01:30 PM): 7B </p>
<p>Note: Days/timings are flexible but have to be allocated before the beginning of the academic year.</p>

<p>We will come in and teach that period each week for the whole academic year.</p>
</div>
<hr>
<h4><a href="##" data-toggle="collapse" data-target=".4">Are the BeeCreative classes related to the curriculum?</a></h4>
<div class="collapse 4">Yes. BC classes are designed to increase your students’ curiosity about what they learn in regular classes. Thus, the classes are organized into clusters, with each cluster directly linked to a specific part of the curriculum.
</div>
<hr>
<h4><a href="##" data-toggle="collapse" data-target=".5">What are clusters?</a></h4>
<div class="collapse 5">Clusters are a group of 6 - 8 classes that result in a coherent learning experience? Each cluster is organized to explore a specific topic or focus area of the curriculum in depth while also building interdisciplinary skills. 
</div>
<hr>
<h4><a href="##" data-toggle="collapse" data-target=".6">Can I choose the clusters my students receive?</a></h4>
<div class="collapse 6">Yes. The BeeCreative program has a large list of clusters that you can choose from. Given that they are between 30 and 40 classes per academic year, you get to choose three to five clusters per year for your school.
</div>
<hr>
<h4><a href="##" data-toggle="collapse" data-target=".7">Who are the teachers?</a></h4>
<div class="collapse 7">Our extensively trained teachers are from a diverse range of fields. Given our history and interests, we have many teachers who were once engineers. At the same time, we also have teachers from fields as diverse as social work and management. 
</div>
<hr>
<h4><a href="##" data-toggle="collapse" data-target=".8">How much does the BeeCreative program cost?</a></h4>
<div class="collapse 8">We are committed to transparent pricing, all our clients should pay exactly the same rate. The cost of the BC program for schools is 650 rupees per student per month for 12 months i.e. 7800 a year per student. This package? includes our teachers, all the material we bring into the classroom, and all learning aids (e.g. charts, AV materials etc). We permit a minimum of 20 students and a maximum of 36 students in a class.
</div>
<hr>
<h4><a href="##" data-toggle="collapse" data-target=".9">Can I negotiate a discount?</a></h4>
<div class="collapse 9">Yes, but it is an easy negotiation because we offer just one kind of discount. For making your full annual payment at the start of the year we offer a 5% discount. Otherwise, we believe that all our clients should pay the same rate. Our pricing is open and transparent. For the 2074 academic year, the fixed price is 650 rupees per student
</div>
